CREATE TABLE MEALS(MEALID VARCHAR(5) PRIMARY KEY,NAME VARCHAR(20),DESCRIPTION VARCHAR(20));
CREATE TABLE MENUITEMS(ITEMID VARCHAR(5) PRIMARY KEY,NAME VARCHAR(20),PRICE NUMERIC(5,2),DATEADDED DATE);
CREATE TABLE PARTOF(MEALID VARCHAR(5),ITEMID VARCHAR(5),QUANTITY INTEGER,DISCOUNT DECIMAL(2,2),PRIMARY KEY(MEALID,ITEMID),FOREIGN KEY (MEALID) REFERENCES MEALS(MEALID),FOREIGN KEY (ITEMID) REFERENCES MENUITEMS(ITEMID));
CREATE TABLE ADS(SLOGAN VARCHAR(50));
CREATE TABLE VENDORS(VENDORID VARCHAR(5) PRIMARY KEY,COMPANYNAME VARCHAR(30),REPFNAME VARCHAR(20),REPLNAME VARCHAR(20),REFFEREDBY VARCHAR(5),FOREIGN KEY (REFFEREDBY) REFERENCES VENDORS(VENDORID));
CREATE TABLE INGREDIENTS(INGREDIENTID VARCHAR(5) PRIMARY KEY,NAME VARCHAR(30),UNIT VARCHAR(10),UNITPRICE NUMERIC(5,2),FOODGROUP VARCHAR(15),INVENTORY INTEGER,VENDORID VARCHAR(5),FOREIGN KEY (VENDORID) REFERENCES VENDORS(VENDORID));
CREATE TABLE MADEWITH(ITEMID VARCHAR(5),INGREDIENTID VARCHAR(5),QUANTITY INTEGER,PRIMARY KEY(ITEMID,INGREDIENTID),FOREIGN KEY (ITEMID) REFERENCES MENUITEMS(ITEMID),FOREIGN KEY (INGREDIENTID) REFERENCES INGREDIENTS(INGREDIENTID));

